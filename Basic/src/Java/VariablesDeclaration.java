package Java;

public class VariablesDeclaration {

	public static void main(String[] args) {
		// different ways to crate/declare variables;
		// 1.create variable and assign value
		int num = 28;

		// 2ways: using 2 steps
		int y; // 1. declaring variable
		y = 12; // 2. assign value

		// declare 1 by 1
		int a;
		int b;
		int c;
		int d;
		int f;
		// or declare all together
		int x, m, z, w, v;

		a = 10;
		b = 20;

		int aa = 10;
		int bb = 20;
		String name = "melek";
		System.out.println(name);

	}

}

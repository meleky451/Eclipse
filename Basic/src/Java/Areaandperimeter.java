package Java;

public class Areaandperimeter {
	
	public static void main (String[] args) {
		
		//declare 2 int type  variables for width and height of a rectangle and then
		//find and print on the screen the area and the perimeter of a rectangle
		
		
		int width=5;
		int height=8;
		
		int area=width*height;
		int perimeter=2*(width+height);
		
		System.out.println("the perimeter of a rectangle with width " + width +  " and height " + height + " is equal to " + perimeter + " and the area is " + area );
		
		
	}
	
	
	
}
